namespace stinky {

    /////////////////////////////////////////////////////////////////////////////////////////
    enum class mouse_code : uint16_t {
        // From glfw3.h
        Button0 = 0,
        Button1 = 1,
        Button2 = 2,
        Button3 = 3,
        Button4 = 4,
        Button5 = 5,
        Button6 = 6,
        Button7 = 7,

        ButtonLast = Button7,
        ButtonLeft = Button0,
        ButtonRight = Button1,
        ButtonMiddle = Button2
    };

    /////////////////////////////////////////////////////////////////////////////////////////
    inline std::ostream &operator<<(std::ostream &os, mouse_code mouseCode) {
        os << static_cast<int32_t>(mouseCode);
        return os;
    }
}
