#pragma once

#include "event/event.h"
#include "core/keyboard_codes.h"

namespace stinky {

    /////////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////////////////////////////////////////////////////
    class key_pressed_event : public event {
    public:
        explicit key_pressed_event(key_code key) : EVENT_CONSTRUCTOR(KeyPressed), key(key) {}

        key_code key;
    };

    /////////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////////////////////////////////////////////////////
    class key_released_event : public event {
    public:
        explicit key_released_event(key_code key) : EVENT_CONSTRUCTOR(KeyReleased), key(key) {}

        key_code key;
    };

    /////////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////////////////////////////////////////////////////
    class key_typed_event : public event {
    public:
        explicit key_typed_event(key_code key) : EVENT_CONSTRUCTOR(KeyTyped), key(key) {}

        key_code key;
    };
}